def editpro(request):
    if request.method=="POST":
        u=request.POST['us']
        f=request.POST['first']
        l=request.POST['last']
        e=request.POST['email']
        m=request.POST['mobile']
        aa=request.POST['aadhar']
        add=request.POST['address']
        myfile = request.FILES['file']
        fs = FileSystemStorage()
        filename = fs.save(myfile.name, myfile)
        uploaded_file_url = fs.url(filename)
        c=connection.cursor()
        c.execute(f"""update auth_user set first_name='{f}',
        	last_name='{l}',email='{e}',mobile='{m}',aadhar='{aa}',
        	address='{add}',pic='{uploaded_file_url}' where username='{u}'""")
        return redirect('home')
    else:    
        return render(request,"Accounts/editpro.html")    

def userpic(request,u):
	query=connection.cursor()
	query.execute(f"select username,first_name,last_name,email,mobile,aadhar,address,pic from auth_user where username='{u}'")
	s=query.fetchone()
	return render(request,"home.html",{'data':s})	

def loginview(request):
    if request.method=="POST":
        form=AuthenticationForm(data=request.POST)
        if form.is_valid():
            user=form.get_user()
            login(request,user)
            return redirect('home')
    else:
        form=AuthenticationForm()
    return render(request,"Accounts/login.html",{'form':form})

